const formatDate = (date) => {
  const day = date.day ? String(date.day).padStart(2, '0') : '';
  const month = date.month ? String(date.month).padStart(2, '0') : '';
  const year = date.year ? date.year : '';

  if (day && month) {
    return `${day}.${month}.${year}`;
  } else if (month) {
    return `${month}.${year}`;
  } else {
    return `${year}`;
  }
};

