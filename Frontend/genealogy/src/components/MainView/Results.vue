        <p v-if="result.peopleDocuments && result.peopleDocuments.length > 0">
          <span class="label">Osoby występujące w dokumencie:</span>
          <span class="value">{{ formatPeopleDocuments(result.peopleDocuments) }}</span>
        </p>
      </div>
    </div>

